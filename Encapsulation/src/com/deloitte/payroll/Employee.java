package com.deloitte.payroll;

import java.sql.Date;

public class Employee {
	
	public Date date;
	
	private int empId;
	private String firstName;
	private String lastName;
	private int deptId;
	public int getEmpId() {
		return empId;
	}
	
	
	public String getFirstName() {
		return firstName;
	}
	
	public String getLastName() {
		return lastName;
	}
	
	public int getDeptId() {
		return deptId;
	}
	
	


}
